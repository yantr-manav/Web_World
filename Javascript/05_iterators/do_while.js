// While Loop
let index = 0 
while(index <= 10){
    // console.log(`Value of index is ${index}`)
    index = index + 2
}

let myArray = ['Flash', 'Batman', 'Superman','Ironman']

let arr = 0
while(arr< myArray.length){
    // console.log(`Value is ${myArray[arr]}`)
    arr = arr + 1
}

// DO-WHILE: work done first before checking any condition
